t=0:0.0001:0.5;
Y1=3*sin(2*pi*t*10);
Y2=0.5*sin(2*pi*t*30);
Y3=sin(2*pi*t*50);
Y4=sin(2*pi*t*150);
figure;
subplot(2,3,1),plot(t,Y1);
title('频率为10Hz');
subplot(2,3,2),plot(t,Y2);
title('频率为30Hz');
subplot(2,3,3),plot(t,Y3);
title('频率为50Hz');
subplot(2,3,4),plot(t,Y4);
title('频率为150Hz');
Y=3*sin(2*pi*t*10)+2*sin(2*pi*t*30)+sin(2*pi*t*50)+sin(2*pi*t*150);
subplot(2,3,5);
plot(t,Y);
title('最终信号');