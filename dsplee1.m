Ts1=1/150;n=0:29;
t=n*Ts1;
x1=3*sin(2*pi*t*10)+2*sin(2*pi*t*30)+sin(2*pi*t*50)+sin(2*pi*t*150);
figure;
subplot(2,3,1),stem(n,x1);
grid on;
title('T>1/2fcN点采样序列');
xlabel('N');
ylabel('Y');
f1=abs(fft(x1));
subplot(2,3,2),stem(n,f1);
title('幅频特性');
xlabel('f(Hz)');
ylabel('Y');
a1=unwrap(angle(fft(x1)));
subplot(2,3,3),stem(n,a1);
title('相频特性');
xlabel('f(Hz)');
ylabel('Y');


